//! # Arduino Modulino LED Matrix Example for Adafruit Feather ESP32-C6
//!
//! Controls the 12x8 LED Matrix on the Arduino Modulino LED Matrix module over I2C.
//!
//! ## Hardware
//!
//! - **Board:** Adafruit Feather ESP32-C6
//! - **Module:** Arduino Modulino LED Matrix
//!
//! ## Wiring with Qwiic/STEMMA QT
//!
//! Simply connect the Qwiic/STEMMA QT cable between the board and the Modulino LED Matrix.
//! The cable provides:
//! ```
//!      Modulino LED Matrix -> Adafruit Feather ESP32-C6
//! (black)  GND -> GND (Stemma GND)
//! (red)    VCC -> 3.3V (Stemma V+)
//! (yellow) SCL -> GPIO 18 (Stemma SCL)
//! (blue)   SDA -> GPIO 19 (Stemma SDA)
//! ```
//!
//! ## Run
//!
//! ```bash
//! cargo run --example modulino_led_matrix_i2c
//! ```

#![no_std]
#![no_main]

use defmt::{Debug2Format, error, info};
use esp_hal::{
    delay::Delay,
    i2c::master::{Config as I2cConfig, I2c as EspI2c},
    main,
    time::Rate,
};
use panic_rtt_target as _;

esp_bootloader_esp_idf::esp_app_desc!();

use embedded_hal::delay::DelayNs;
use embedded_hal::i2c::I2c;

use modulino::{DisplayMode, LedMatrix};

/// Linear Congruential Generator (LCG) for bare-metal pseudo-random numbers
struct Lcg {
    state: u32,
}

impl Lcg {
    fn new(seed: u32) -> Self {
        Self { state: seed }
    }

    fn next(&mut self) -> u32 {
        self.state = self.state.wrapping_mul(1664525).wrapping_add(1013904223);
        self.state
    }

    fn next_float(&mut self) -> f32 {
        (self.next() as f32) / (u32::MAX as f32)
    }

    fn next_range(&mut self, min: f32, max: f32) -> f32 {
        min + self.next_float() * (max - min)
    }
}

// 5x7 Character glyph representation for 12x8 marquee text.
// Each character is 5 columns wide. Each byte in the array is a column,
// where bits 0 to 6 represent rows 0 to 6.
const GLYPH_R: [u8; 5] = [0x7F, 0x09, 0x11, 0x21, 0x46];
const GLYPH_U: [u8; 5] = [0x3F, 0x40, 0x40, 0x40, 0x3F];
const GLYPH_S: [u8; 5] = [0x46, 0x49, 0x49, 0x49, 0x31];
const GLYPH_T: [u8; 5] = [0x01, 0x01, 0x7F, 0x01, 0x01];
const GLYPH_SPACE: [u8; 5] = [0x00, 0x00, 0x00, 0x00, 0x00];

#[main]
fn main() -> ! {
    rtt_target::rtt_init_defmt!();

    let peripherals = esp_hal::init(esp_hal::Config::default());
    let mut delay = Delay::new();

    info!("Initializing Modulino LED Matrix...");

    // Power on the I2C / NeoPixel port (GPIO 20)
    info!("Enabling I2C / NeoPixel Power (GPIO 20)");
    let _pwr = esp_hal::gpio::Output::new(
        peripherals.GPIO20,
        esp_hal::gpio::Level::High,
        esp_hal::gpio::OutputConfig::default(),
    );

    // Give the hardware a moment to boot up after receiving power
    delay.delay_millis(50);

    // Configure I2C pins
    let sda = peripherals.GPIO19;
    let scl = peripherals.GPIO18;

    // Create I2C peripheral
    let i2c_config = I2cConfig::default().with_frequency(Rate::from_khz(100));
    let i2c = EspI2c::new(peripherals.I2C0, i2c_config)
        .unwrap()
        .with_sda(sda)
        .with_scl(scl);

    info!("Initializing Modulino LED Matrix via library driver...");

    // Create and initialize the matrix
    let mut matrix = LedMatrix::new(i2c);
    if let Err(e) = matrix.init() {
        error!(
            "Failed to initialize Modulino LED Matrix: {:?}",
            Debug2Format(&e)
        );
        loop {
            delay.delay_millis(1000);
        }
    }

    info!("Modulino LED Matrix successfully initialized!");

    // Helper for pseudo-random number generator
    let mut rng = Lcg::new(42);

    loop {
        // Animation 1: Larson Scanner (Cylon eye sweep)
        info!("Animation 1: Larson Scanner");
        larson_scanner(&mut matrix, &mut delay);

        // Animation 2: Sinusoidal Wave
        info!("Animation 2: Sinusoidal Wave");
        sine_wave(&mut matrix, &mut delay);

        // Animation 3: Digital Matrix Rain
        info!("Animation 3: Matrix Rain");
        matrix_rain(&mut matrix, &mut delay, &mut rng);

        // Animation 4: Breathing Smiley Face
        info!("Animation 4: Breathing Smiley Face");
        breathing_face(&mut matrix, &mut delay);

        // Animation 5: Horizontal Scrolling Marquee Text ("RUST")
        info!("Animation 5: Scrolling Marquee Text");
        scrolling_text(&mut matrix, &mut delay);

        // Animation 6: Sparkling Dissolve Rust Logo
        info!("Animation 6: Sparkling Dissolve Rust Logo");
        rust_logo_animation(&mut matrix, &mut delay, &mut rng);

        // Animation 7: Grayscale Flame Simulation
        info!("Animation 7: Grayscale Flame Simulation");
        grayscale_flame(&mut matrix, &mut delay, &mut rng);
    }
}

/// Sweeps a vertical bar back and forth across the 12 columns
fn larson_scanner<I2C, E>(matrix: &mut LedMatrix<I2C>, timer: &mut impl DelayNs)
where
    I2C: I2c<Error = E>,
{
    let mut head = 0i32;
    let mut direction = 1;

    for _ in 0..48 {
        let mut frame = [0u8; 12];

        // Draw a double-column vertical bar at the scanner head
        if (0..12).contains(&head) {
            frame[head as usize] = 0xFF;
        }
        if (0..12).contains(&(head + 1)) {
            frame[(head + 1) as usize] = 0xFF;
        }

        matrix.set_frame(&frame).ok();
        timer.delay_ms(60);

        head += direction;
        if head == 10 {
            direction = -1;
        } else if head == 0 {
            direction = 1;
        }
    }
}

/// Displays a smooth scrolling sinusoidal wave on the 12x8 grid
fn sine_wave<I2C, E>(matrix: &mut LedMatrix<I2C>, timer: &mut impl DelayNs)
where
    I2C: I2c<Error = E>,
{
    for step in 0..120 {
        let mut frame = [0u8; 12];

        for x in 0..12u8 {
            let x_factor = x as f32 * 0.6;
            let time_factor = step as f32 * 0.2;
            let wave_y = 3.5 + 3.0 * libm::sinf(x_factor - time_factor);

            // Turn on the row pixel nearest to the sine line
            let y = (wave_y + 0.5f32) as i32;
            if (0..8).contains(&y) {
                frame[x as usize] |= 1 << y;
            }
        }

        matrix.set_frame(&frame).ok();
        timer.delay_ms(40);
    }
}

/// Realistic falling digital rain droplets
fn matrix_rain<I2C, E>(matrix: &mut LedMatrix<I2C>, timer: &mut impl DelayNs, rng: &mut Lcg)
where
    I2C: I2c<Error = E>,
{
    let mut heads = [0.0f32; 12];
    let mut speeds = [0.0f32; 12];

    for i in 0..12 {
        heads[i] = rng.next_range(-6.0, 0.0);
        speeds[i] = rng.next_range(0.15, 0.4);
    }

    for _ in 0..180 {
        let mut frame = [0u8; 12];

        for x in 0..12u8 {
            let head_y = heads[x as usize];

            // Render a droplet tail (3 rows long)
            for y in 0..8u8 {
                let dist = head_y - y as f32;
                if (0.0..3.0).contains(&dist) {
                    frame[x as usize] |= 1 << y;
                }
            }

            // Advance the droplet
            heads[x as usize] += speeds[x as usize];

            // Reset when droplet fully leaves the bottom (row 8)
            if heads[x as usize] - 3.0 > 8.0 {
                heads[x as usize] = rng.next_range(-4.0, 0.0);
                speeds[x as usize] = rng.next_range(0.15, 0.4);
            }
        }

        matrix.set_frame(&frame).ok();
        timer.delay_ms(35);
    }
}

/// Smiley face that pulses/blinks
fn breathing_face<I2C, E>(matrix: &mut LedMatrix<I2C>, timer: &mut impl DelayNs)
where
    I2C: I2c<Error = E>,
{
    // 12x8 Smiley face template
    let smiley_frame = [
        0x00, // Col 0
        0x10, // Col 1: Smile start
        0x26, // Col 2: Left Eye + Smile curve
        0x40, // Col 3: Smile bottom
        0x40, // Col 4
        0x40, // Col 5
        0x40, // Col 6
        0x40, // Col 7
        0x40, // Col 8
        0x26, // Col 9: Right Eye + Smile curve
        0x10, // Col 10: Smile end
        0x00, // Col 11
    ];

    let empty_frame = [0u8; 12];

    // Blink the face 4 times
    for _ in 0..4 {
        matrix.set_frame(&smiley_frame).ok();
        timer.delay_ms(600);
        matrix.set_frame(&empty_frame).ok();
        timer.delay_ms(250);
    }
}

/// Scrolls "RUST" horizontally across the 12-column screen
fn scrolling_text<I2C, E>(matrix: &mut LedMatrix<I2C>, timer: &mut impl DelayNs)
where
    I2C: I2c<Error = E>,
{
    // The message is "RUST " (5 characters, each is 5 columns + 1 space = 6 columns per character)
    const MSG_LEN: usize = 5;
    let glyphs: [&[u8; 5]; MSG_LEN] = [&GLYPH_R, &GLYPH_U, &GLYPH_S, &GLYPH_T, &GLYPH_SPACE];

    // Build the marquee column buffer (5 * 6 = 30 columns)
    let mut marquee_buffer = [0u8; MSG_LEN * 6];
    let mut idx = 0;
    for glyph in glyphs.iter() {
        for col_val in glyph.iter() {
            marquee_buffer[idx] = *col_val;
            idx += 1;
        }
        marquee_buffer[idx] = 0x00; // 1-column spacing
        idx += 1;
    }

    let total_cols = marquee_buffer.len();

    // Scroll across the 12-column display screen (30 + 12 = 42 shifts)
    for shift in 0..(total_cols + 12) {
        let mut frame = [0u8; 12];

        for x in 0..12u8 {
            let col_index = (shift as i32) + (x as i32) - 12;
            if col_index >= 0 && col_index < total_cols as i32 {
                let glyph_col = marquee_buffer[col_index as usize];
                // Shift by 1 bit to vertically center the 7-row glyph inside the 8-row height
                frame[x as usize] = glyph_col << 1;
            }
        }

        matrix.set_frame(&frame).ok();
        timer.delay_ms(100);
    }
}

/// Displays a beautifully framed, high-fidelity capital "R" in the center of a
/// rectangular border with a sparkling dissolve reveal and dissolve erase sequence!
fn rust_logo_animation<I2C, E>(matrix: &mut LedMatrix<I2C>, timer: &mut impl DelayNs, rng: &mut Lcg)
where
    I2C: I2c<Error = E>,
{
    // To make the logo completely recognizable and beautiful on the 12x8 LED matrix,
    // we frame a slightly smaller bold capital 'R' inside a perfectly proportioned
    // rectangular border. To prevent it from looking like an 'A', the loop is thinned
    // on the right side (ends at Col 7) and the diagonal leg is kicked out (at Col 8).
    // This creates a highly clean, structured, and modern look with 100% legibility.
    let target_frame = [
        0xFF, // Col 0: Left border (Rows 0..7)
        0x81, // Col 1: Empty spacer + border top/bottom (Rows 0, 7)
        0x81, // Col 2: Empty spacer + border top/bottom (Rows 0, 7)
        0xBD, // Col 3: Thick 'R' stem left (Rows 2,3,4,5) + border top/bottom (Rows 0, 7)
        0xBD, // Col 4: Thick 'R' stem right (Rows 2,3,4,5) + border top/bottom (Rows 0, 7)
        0x95, // Col 5: 'R' loop top/middle (Rows 2, 4) + border top/bottom (Rows 0, 7)
        0x95, // Col 6: 'R' loop top/middle (Rows 2, 4) + border top/bottom (Rows 0, 7)
        0x9D, // Col 7: 'R' loop right side (Rows 2,3,4) + border top/bottom (Rows 0, 7)
        0xA1, // Col 8: 'R' sloped diagonal leg (Row 5) + border top/bottom (Rows 0, 7)
        0x81, // Col 9: Empty spacer + border top/bottom (Rows 0, 7)
        0x81, // Col 10: Empty spacer + border top/bottom (Rows 0, 7)
        0xFF, // Col 11: Right border (Rows 0..7)
    ];

    // 2. Collect all active pixel coordinates
    let mut coords = [(0u8, 0u8); 64];
    let mut count = 0;

    for x in 0..12u8 {
        for y in 0..8u8 {
            if (target_frame[x as usize] & (1 << y)) != 0 {
                coords[count] = (x, y);
                count += 1;
            }
        }
    }

    // 3. Shuffle the active pixel coordinates
    // Fisher-Yates shuffle
    for i in (1..count).rev() {
        let j = (rng.next() as usize) % (i + 1);
        coords.swap(i, j);
    }

    // 4. Sparkle/dissolve reveal
    let mut current_frame = [0u8; 12];
    matrix.set_frame(&current_frame).ok();

    for &(x, y) in coords.iter().take(count) {
        current_frame[x as usize] |= 1 << y;
        matrix.set_frame(&current_frame).ok();
        timer.delay_ms(30);
    }

    // Keep the full logo lit for 2.5 seconds
    timer.delay_ms(2500);

    // 5. Sparkle/dissolve erase
    // Let's shuffle again so the erase sequence is different from the reveal!
    for i in (1..count).rev() {
        let j = (rng.next() as usize) % (i + 1);
        coords.swap(i, j);
    }

    for &(x, y) in coords.iter().take(count) {
        current_frame[x as usize] &= !(1 << y);
        matrix.set_frame(&current_frame).ok();
        timer.delay_ms(25);
    }

    // Clear and delay at the end
    let empty = [0u8; 12];
    matrix.set_frame(&empty).ok();
    timer.delay_ms(500);
}

/// Plays the official high-fidelity C++ grayscale flame animation frames on the 12x8 matrix.
fn grayscale_flame<I2C, E>(matrix: &mut LedMatrix<I2C>, timer: &mut impl DelayNs, _rng: &mut Lcg)
where
    I2C: I2c<Error = E>,
    E: core::fmt::Debug,
{
    // Switch to Grayscale mode
    if let Err(e) = matrix.set_mode(DisplayMode::Grayscale) {
        error!("Failed to switch to grayscale mode: {:?}", Debug2Format(&e));
        return;
    }
    timer.delay_ms(10);

    // Loop through all 269 frames of the official animation
    for frame in FLAMES.iter() {
        if let Err(e) = matrix.set_frame(&frame[..48]) {
            error!("Failed to write grayscale frame: {:?}", Debug2Format(&e));
            break;
        }
        timer.delay_ms(45);
    }

    // Switch back to Monochromatic mode
    if let Err(e) = matrix.set_mode(DisplayMode::MonochromaticVertical) {
        error!(
            "Failed to switch to monochromatic mode: {:?}",
            Debug2Format(&e)
        );
    }
    timer.delay_ms(10);
}

